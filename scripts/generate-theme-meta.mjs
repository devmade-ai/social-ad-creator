// Requirement: Auto-generate hex meta theme-color values from DaisyUI's oklch theme definitions.
// Approach: Read DaisyUI's theme objects, convert oklch→hex, and update daisyuiThemes.js + index.html.
//   Light themes use --color-primary, dark themes use --color-base-100.
//   Light/dark classification derived from each theme's color-scheme property.
// Alternatives:
//   - Manual hex values: Rejected — error-prone, goes stale on DaisyUI updates.
//   - Runtime conversion: Rejected — adds bundle size, flash-prevention script can't import modules.
//
// Run: node scripts/generate-theme-meta.mjs
// Run after: DaisyUI version updates, or adding/removing curated themes.

import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'
import { createRequire } from 'module'
import { oklchToHex } from './oklchToHex.mjs'

const require = createRequire(import.meta.url)
const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = resolve(__dirname, '..')

// ---------------------------------------------------------------------------
// 2. Read DaisyUI theme objects and extract hex colors
// ---------------------------------------------------------------------------

const daisyuiThemes = require('daisyui/theme/object.js').default

// Combo definitions — each combo pairs a light and dark theme.
const COMBOS = [
  { id: 'mono', name: 'Mono', description: 'Clean & minimal', light: 'lofi', dark: 'black' },
  { id: 'luxe', name: 'Luxe', description: 'Rich & elegant', light: 'fantasy', dark: 'luxury' },
]

const DEFAULT_COMBO = 'luxe'

// Per-theme color key overrides. Default: dark → base-100, light → primary.
// Some light themes have primary colors that don't represent the theme feel.
const COLOR_KEY_OVERRIDES = {
  lofi: '--color-base-300',        // Monochrome theme — neutral gray > near-black primary
}

function getHexForTheme(id) {
  const theme = daisyuiThemes[id]
  if (!theme) throw new Error(`Theme "${id}" not found in DaisyUI`)

  const scheme = theme['color-scheme']
  const defaultKey = scheme === 'dark' ? '--color-base-100' : '--color-primary'
  const colorKey = COLOR_KEY_OVERRIDES[id] || defaultKey
  const oklch = theme[colorKey]
  if (!oklch) throw new Error(`Theme "${id}" missing ${colorKey}`)

  const hex = oklchToHex(oklch)
  if (!hex) throw new Error(`Failed to convert ${oklch} for theme "${id}"`)

  return hex
}

// ---------------------------------------------------------------------------
// 3. Generate all hex values
// ---------------------------------------------------------------------------

const comboEntries = COMBOS.map(combo => ({
  ...combo,
  lightMeta: getHexForTheme(combo.light),
  darkMeta: getHexForTheme(combo.dark),
}))

const defaultCombo = comboEntries.find(c => c.id === DEFAULT_COMBO)

console.log('Generated meta theme-colors:')
comboEntries.forEach(c => {
  console.log(`  ${c.id}: light=${c.light} (${c.lightMeta}), dark=${c.dark} (${c.darkMeta})`)
})
console.log(`Default combo: ${DEFAULT_COMBO}`)

// ---------------------------------------------------------------------------
// 4. Update daisyuiThemes.js
// ---------------------------------------------------------------------------

function generateComboEntry(entry) {
  return `  {
    id: '${entry.id}',
    name: '${entry.name}',
    description: '${entry.description}',
    light: { id: '${entry.light}', metaColor: '${entry.lightMeta}' },
    dark: { id: '${entry.dark}', metaColor: '${entry.darkMeta}' },
  },`
}

const themeFileContent = `// Requirement: Paired theme combos for simplified DaisyUI theme selection.
// Approach: Two curated combos (Mono + Luxe), each pairing a light and dark theme.
//   User picks a combo; dark/light toggle switches between the combo's themes.
// Alternatives:
//   - Independent per-mode selection (16 themes): Rejected — too many choices, simplified to combos.
//   - Single theme with no combo: Rejected — user wants at least two visual options.

// Meta theme-color for PWA status bar — matches each theme's primary or base color.
// AUTO-GENERATED by scripts/generate-theme-meta.mjs from DaisyUI oklch values.
// Do not edit metaColor values manually — run the script after DaisyUI updates.

export const themeCombos = [
${comboEntries.map(generateComboEntry).join('\n')}
]

export const DEFAULT_COMBO = '${DEFAULT_COMBO}'

// Look up meta theme-color for a given theme ID.
// Falls back to neutral values if theme not found.
export function getMetaColor(themeId) {
  for (const combo of themeCombos) {
    if (combo.light.id === themeId) return combo.light.metaColor
    if (combo.dark.id === themeId) return combo.dark.metaColor
  }
  return '#808080'
}

// Look up combo by ID — returns combo object or default combo.
export function getCombo(comboId) {
  return themeCombos.find(c => c.id === comboId) || themeCombos[0]
}
`

writeFileSync(resolve(rootDir, 'src/config/daisyuiThemes.js'), themeFileContent)
console.log('\n✓ Updated src/config/daisyuiThemes.js')

// ---------------------------------------------------------------------------
// 5. Update index.html — meta tag and inline flash-prevention script
// ---------------------------------------------------------------------------

let html = readFileSync(resolve(rootDir, 'index.html'), 'utf-8')

// Three regions in index.html are auto-generated, delimited by GEN: sentinels.
// Matching between sentinels (rather than against literal JS shapes like
// `var combos = ...`) keeps the generator robust to refactors of surrounding
// code and signals to readers that the region is owned by this script.
// The pre-React inline flash-prevention script and meta tag live in
// <head> and must stay byte-stable for first-paint correctness — sentinels
// also make it impossible for an unrelated edit to accidentally clobber
// generated output.

function replaceBetweenSentinels(input, name, replacement) {
  // Sentinel pairs: HTML form (<!-- GEN:name --> ... <!-- GEN:end -->) and
  // JS-comment form (/* GEN:name */ ... /* GEN:end */). Both are matched here
  // because the generated regions live in different syntax contexts within
  // index.html (HTML body vs inline <script>).
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const htmlRe = new RegExp(`(<!-- GEN:${escaped} -->)([\\s\\S]*?)(<!-- GEN:end -->)`)
  const jsRe = new RegExp(`(/\\* GEN:${escaped} \\*/)([\\s\\S]*?)(/\\* GEN:end \\*/)`)
  if (htmlRe.test(input)) return input.replace(htmlRe, `$1\n    ${replacement}\n    $3`)
  if (jsRe.test(input)) return input.replace(jsRe, `$1 ${replacement} $3`)
  throw new Error(`GEN sentinel "${name}" not found in index.html — cannot rewrite. Add <!-- GEN:${name} --> ... <!-- GEN:end --> or /* GEN:${name} */ ... /* GEN:end */ around the target region.`)
}

html = replaceBetweenSentinels(html, 'theme-color-meta', `<meta name="theme-color" content="${defaultCombo.lightMeta}" />`)

// Update inline combo map in flash-prevention script
const comboMap = {}
comboEntries.forEach(c => { comboMap[c.id] = { light: c.light, dark: c.dark } })

// Update meta color map
const allColorMap = {}
comboEntries.forEach(c => {
  allColorMap[c.light] = c.lightMeta
  allColorMap[c.dark] = c.darkMeta
})
const colorMapStr = JSON.stringify(allColorMap)

html = replaceBetweenSentinels(html, 'combo-map', `var combos = ${JSON.stringify(comboMap)};`)
html = replaceBetweenSentinels(html, 'meta-colors', `var metaColors = ${colorMapStr};`)

writeFileSync(resolve(rootDir, 'index.html'), html)
console.log('✓ Updated index.html (meta tag + inline script)')

// ---------------------------------------------------------------------------
// 6. Rewrite icon.svg gradient stops from default-combo light theme primary
// ---------------------------------------------------------------------------
//
// The PWA icon (public/icon.svg → 5 PNG variants) uses a four-stop gradient
// derived from the default light theme's primary color (currently fantasy).
// Hardcoding hex values here would let the icon drift out of sync the moment
// DaisyUI nudges the primary's oklch — auto-generation keeps them locked.
//
// Design intent (encoded as L/C deltas on top of the primary's oklch):
//   bg gradient: a slightly lighter primary → primary itself
//   accent gradient: even lighter, less-saturated tints used at low opacity
// Hue is preserved across all four. Chroma is preserved except for the
// accent-start, which drops slightly so the lightest tint reads as a tint
// (not a fully-saturated lighter color).

const ICON_DELTAS = [
  { name: 'bg.start',     dL:  12.55, dC:  0      },
  { name: 'bg.end',       dL:   0,    dC:  0      },
  { name: 'accent.start', dL:  32.55, dC: -0.039 },
  { name: 'accent.end',   dL:  22.55, dC:  0      },
]

function deriveIconColors(primaryOklch) {
  const m = primaryOklch.match(/oklch\(\s*([\d.]+)%\s+([\d.]+)\s+([\d.]+)/)
  if (!m) throw new Error(`Cannot parse primary oklch: ${primaryOklch}`)
  const L = parseFloat(m[1])
  const C = parseFloat(m[2])
  const H = parseFloat(m[3])

  return ICON_DELTAS.map(({ name, dL, dC }) => {
    const newL = Math.max(0, Math.min(100, L + dL))
    const newC = Math.max(0, C + dC)
    const oklch = `oklch(${newL}% ${newC} ${H})`
    const hex = oklchToHex(oklch)
    if (!hex) throw new Error(`Failed to convert ${oklch} for ${name}`)
    return { name, hex }
  })
}

const lightThemeId = defaultCombo.light
const lightTheme = daisyuiThemes[lightThemeId]
const primaryOklch = lightTheme['--color-primary']
const iconColors = deriveIconColors(primaryOklch)

console.log(`\nDerived icon colors from ${lightThemeId} primary (${primaryOklch}):`)
iconColors.forEach(c => console.log(`  ${c.name.padEnd(14)} ${c.hex}`))

const iconPath = resolve(rootDir, 'public/icon.svg')
let svg = readFileSync(iconPath, 'utf-8')

// Replace all four stop-color values positionally. The icon's <defs> contains
// exactly four <stop> elements in the order: bg.start, bg.end, accent.start,
// accent.end. A counter-based replacer assigns the new colors in that order.
// Pre-flight check: confirm there are exactly four stops so a structural
// regression in icon.svg fails loudly rather than silently corrupting colors.
const stopMatches = svg.match(/stop-color="#[0-9a-f]{6}"/gi) || []
if (stopMatches.length !== ICON_DELTAS.length) {
  throw new Error(
    `Expected ${ICON_DELTAS.length} <stop> elements in icon.svg, found ${stopMatches.length}. ` +
    `Update generate-theme-meta.mjs ICON_DELTAS or fix icon.svg structure.`
  )
}

let stopIdx = 0
svg = svg.replace(/stop-color="#[0-9a-f]{6}"/gi, () => {
  return `stop-color="${iconColors[stopIdx++].hex}"`
})
writeFileSync(iconPath, svg)
console.log('✓ Updated public/icon.svg gradient stops')

console.log('\nDone. All theme-color and icon values are now in sync.')
console.log('Run `npm run generate-icons` to rasterize the new SVG to PNG variants.')
